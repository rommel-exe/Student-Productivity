import fs from "fs";
import path from "path";

function findPngs(dir: string, fileList: string[] = []): string[] {
  if (!fs.existsSync(dir)) return fileList;
  const files = fs.readdirSync(dir);
  for (const file of files) {
    const filePath = path.join(dir, file);
    try {
      const stat = fs.statSync(filePath);
      if (stat.isDirectory()) {
        if (file !== "node_modules" || dir === process.cwd()) {
          findPngs(filePath, fileList);
        }
      } else if (file.toLowerCase().endsWith(".png")) {
        fileList.push(filePath);
      }
    } catch {}
  }
  return fileList;
}

const nmDir = path.join(process.cwd(), "node_modules");
const allPngs: string[] = [];
if (fs.existsSync(nmDir)) {
  const rootFiles = fs.readdirSync(nmDir);
  for (const rf of rootFiles) {
    const rfPath = path.join(nmDir, rf);
    try {
      const stat = fs.statSync(rfPath);
      if (stat.isDirectory()) {
        findPngs(rfPath, allPngs);
      }
    } catch {}
  }
}

console.log(`Found ${allPngs.length} total PNG files in node_modules:`);
const candidates = allPngs.filter(f => {
  try {
    const stat = fs.statSync(f);
    if (stat.size < 5000) return false;
    const buf = fs.readFileSync(f);
    const isValid = buf[0] === 0x89 && buf[1] === 0x50 && buf[2] === 0x4e && buf[3] === 0x47;
    return isValid;
  } catch {
    return false;
  }
});

console.log(`Filtered ${candidates.length} candidate valid PNGs larger than 5KB:`);
for (const c of candidates) {
  const relativePath = path.relative(process.cwd(), c);
  const size = fs.statSync(c).size;
  console.log(`- ${relativePath}: Size = ${size} bytes`);
}
